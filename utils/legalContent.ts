export const getLegalDocument = (type: 'privacy' | 'terms' | 'cookies'): LegalDocument => {
  switch (type) {
    case 'privacy':
      return PRIVACY_POLICY;
    case 'terms':
      return TERMS_OF_SERVICE;
    case 'cookies':
      return COOKIE_POLICY;
    default:
      return TERMS_OF_SERVICE;
  }
};

export const formatLegalContent = (document: LegalDocument): string => {
  return `${document.title}\n\nVersion: ${document.version}\nLast Updated: ${document.lastUpdated}\n\n${document.content}`;
};
